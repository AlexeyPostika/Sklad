    public int FindStartStopPattern(int[] blocks, ref int where)
    {
      int width = this.image.GetWidth();
      int num = 0;
      int[] pattern = blocks;
      while (num + 8 < width && Pdf417Symbol.CheckPatternBlockLengths(pattern))
      {
        if (Pdf417Symbol.CheckPatternIsStartLocator(pattern))
        {
          where = num;
          return 1;
        }
        if (Pdf417Symbol.CheckPatternIsEndLocator(pattern))
        {
          where = num;
          return 2;
        }
        ++num;
        pattern = new int[blocks.Length - num];
        for (int index = 0; index < pattern.Length; ++index)
          pattern[index] = blocks[index + num];
      }
